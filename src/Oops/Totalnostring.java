package Oops;

public class Totalnostring {

	public static void main(String[] args) {
	  
		        String s="i love my country";    
		        int c=0;    
		        for(int i=0; i< s.length();i++) {    
		            if(s.charAt(i)!=' ')    
		                c++;    
		        }    
		            
		        
		        System.out.println("Total number of characters in a string: " +c);    
		    }    
		

}
